function printName(name) {
  console.log(name);
}
printName("Aritra");

function printInfo(name, age) {
  console.log(`${name}'s age is ${age}`);
}
printInfo("Aritra", 20);
printInfo("Raj", 19);
printInfo("Kanak");
printInfo(14);

function sum(a, b) {
  console.log(`sum is ${a + b}`);
}
sum(5, 2);
sum(1, 2);
