//Arithmatic operator
let a = 10;
let b = 5;
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);
console.log(a ** b);
//Unary operator
console.log(a++);
console.log(++a);
console.log(b--);
console.log(--b);
//Assignment operator
let c = 10;
console.log((c += 1));
console.log((c -= 1));
console.log((c *= 1));
console.log((c /= 1));
console.log((c %= 1));
