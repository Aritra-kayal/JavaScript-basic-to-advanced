let day = 6;
switch (day) {
  case 1:
    console.log("mon");
    break;
  case 2:
    console.log("tu");
    break;
  case 3:
    console.log("wen");
    break;
  case 4:
    console.log("thr");
    break;
  case 5:
    console.log("fri");
    break;
  case 6:
    console.log("sat");
    break;
  case 7:
    console.log("sun");
    break;
  default:
    console.log("not a right day");
}
