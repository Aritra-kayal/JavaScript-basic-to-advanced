//method in JavaScript
let math = {
  add: function (a, b) {
    return a + b;
  },
  sub: function (a, b) {
    return a - b;
  },
  mul: function (a, b) {
    return a * b;
  },
  div: function (a, b) {
    return a / b;
  },
  mod: function (a, b) {
    return a % b;
  },
};
console.log(math.add(2, 3));
